    function stackedbar(projectName,dataUrgent,dataSerious,dataOrdinary,dataSlight){

    var option = {
        title: {
            text: '项目缺陷分布'
        },
        tooltip : {
            trigger: 'axis',
            axisPointer : {            // 坐标轴指示器，坐标轴触发有效
                type : 'shadow'        // 默认为直线，可选为：'line' | 'shadow'
            }
        },
        legend: {
            data: ['紧急', '严重','普通','轻微']
        },
        grid: {
            left: '3%',
            right: '4%',
            bottom: '3%',
            containLabel: true
        },
        xAxis:  {
            type: 'value'
        },
        yAxis: {
            type: 'category',
            data: projectName//项目名称
        },
        series: [
            {
                name: '紧急',
                type: 'bar',
                stack: '总量',
                label: {
                    normal: {
                        show: true,
                        position: 'insideRight'
                    }
                },
                data: dataUrgent//紧急缺陷
            },
            {
                name: '严重',
                type: 'bar',
                stack: '总量',
                label: {
                    normal: {
                        show: true,
                        position: 'insideRight'
                    }
                },
                data: dataSerious//严重缺陷
            },
            {
                name: '普通',
                type: 'bar',
                stack: '总量',
                label: {
                    normal: {
                        show: true,
                        position: 'insideRight'
                    }
                },
                data: dataOrdinary//普通缺陷
            },
            {
                name: '轻微',
                type: 'bar',
                stack: '总量',
                label: {
                    normal: {
                        show: true,
                        position: 'insideRight'
                    }
                },
                data: dataSlight//轻微缺陷
            }
        ]
    };
    return option;
}

